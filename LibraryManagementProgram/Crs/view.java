    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        String sql = "select * from view where IDBook=? and ID=?";
        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, id);
            pst.setString(2, Nid);
            rs = pst.executeQuery();
            if (rs.next()) {
                if (JOptionPane.showConfirmDialog(null, "Do you want change return date ?",
                        "Message",
                        JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION) {
                    try {
                        sql = "UPDATE `librarymanagement`.`view` SET `redate` = ? WHERE (`IDBook` = ?);";
                        pst = conn.prepareStatement(sql);
                        pst.setString(2, jTextField1.getText());
                        pst.setString(1, jTextField3.getText());
                        pst.execute();
                        JOptionPane.showMessageDialog(null, "Return date was change");
                        jTextField1.setText("");
                        jTextField3.setText("");
                        jTextField2.setText("");
                        if (JOptionPane.OK_OPTION == JOptionPane.OK_OPTION) {
                            showTable(Nid);
                        }
                        rs.close();
                        pst.close();

                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(null, e);

                    }
                }
            } else {
                try {
                    sql = "Insert into view values(?,?,?,?)";
                    pst = conn.prepareStatement(sql);
                    pst.setString(1, Nid);
                    pst.setString(2, jTextField1.getText());
                    pst.setString(3, jTextField2.getText());
                    pst.setString(4, jTextField3.getText());
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "New borrow add");
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    if (JOptionPane.OK_OPTION == JOptionPane.OK_OPTION) {
                        showTable(Nid);
                    }
                    rs.close();
                    pst.close();

                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, e);

                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

