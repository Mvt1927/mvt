    /**
     * @param args the command line arguments
     */
    public void showTable(String a) {
        try {
            String sql = "select Title, paydate, redate "
                    + "from view v inner join book b "
                    + "on v.IDBook = b.IDBook where (ID='"+a+"')";
            PreparedStatement st = conn.prepareStatement(sql);
            ResultSet rs = st.executeQuery();
            model.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("Title"),
                    rs.getString("paydate"),
                    rs.getString("redate")
                };
                model.addRow(row);

            }
            rs.close();
            st.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

