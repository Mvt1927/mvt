    /**
     * @param args the command line arguments
     */
    public void showTable() {
        try {
            String sql = "Select * FROM book";
            PreparedStatement st = conn.prepareStatement(sql);
            ResultSet rs = st.executeQuery();
            model.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("IDBook"),
                    rs.getString("Title"),
                    rs.getString("Author"),
                    rs.getString("Status")
                };
                model.addRow(row);

            }
            rs.close();
            st.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

