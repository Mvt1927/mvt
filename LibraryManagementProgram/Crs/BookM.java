    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int selectrow = jTable1.getSelectedRow();
        if (selectrow >= 0) {
            jTextField1.setText(jTable1.getValueAt(selectrow, 0).toString());
            jTextField2.setText(jTable1.getValueAt(selectrow, 1).toString());
            jTextField3.setText(jTable1.getValueAt(selectrow, 2).toString());
            String st = jTable1.getValueAt(selectrow, 3).toString();
            if (st.equals("Still")) {
                jCheckBox1.setSelected(true);
            } else {
                jCheckBox1.setSelected(false);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(null, "Do you want to delete book ? ",
                "Message",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION) {

            String id = jTextField1.getText();
            String sql = "select * from book where IDBook=?";
            try {
                pst = conn.prepareStatement(sql);
                pst.setString(1, id);
                rs = pst.executeQuery();
                if (rs.next()) {
                    try {
                        sql = "DELETE FROM book WHERE (IDBook = ?)";
                        pst1 = conn.prepareStatement(sql);
                        pst1.setString(1, id);
                        pst1.executeUpdate();
                        pst1.close();
                        JOptionPane.showMessageDialog(null, "Book was deleted");
                        jTextField1.setText("");
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jCheckBox1.setSelected(false);
                        showTable();
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(null, "Book was not deleted");

                    }
                    pst.close();
                    rs.close();
                } else {
                    JOptionPane.showMessageDialog(null, "No valuse to delete");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        String sql = "select * from book where IDBook=?";
        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, id);
            rs = pst.executeQuery();
            if (rs.next()) {
                if (JOptionPane.showConfirmDialog(null, "Already have this book id do you want to overwrite?",
                        "Message",
                        JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION) {
                    try {
                        sql = "UPDATE `librarymanagement`.`book` SET `Title` = ?, `Author` = ?, `Status` = ? WHERE (`IDBook` = ?);";
                        pst = conn.prepareStatement(sql);
                        pst.setString(4, jTextField1.getText());
                        pst.setString(1, jTextField2.getText());
                        pst.setString(2, jTextField3.getText());
                        if (jCheckBox1.isSelected()) {
                            pst.setString(3, "Still");
                        } else {
                            pst.setString(3, "Over");
                        }
                        pst.execute();
                        JOptionPane.showMessageDialog(null, "book was change");
                        jTextField1.setText("");
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jCheckBox1.setSelected(false);
                        if (JOptionPane.OK_OPTION == JOptionPane.OK_OPTION) {
                            showTable();
                        }
                        rs.close();
                        pst.close();

                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(null, e);

                    }
                }
            } else {
                try {
                    sql = "Insert into book values(?,?,?,?)";
                    pst = conn.prepareStatement(sql);
                    pst.setString(1, jTextField1.getText());
                    pst.setString(2, jTextField2.getText());
                    pst.setString(3, jTextField3.getText());
                    if (jCheckBox1.isSelected()) {
                        pst.setString(4, "Still");
                    } else {
                        pst.setString(4, "Over");
                    }
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "New book add");
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jCheckBox1.setSelected(false);
                    if (JOptionPane.OK_OPTION == JOptionPane.OK_OPTION) {
                        showTable();
                    }
                    rs.close();
                    pst.close();

                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, e);

                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

